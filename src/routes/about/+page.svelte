	<article class="max-w-7xl mx-auto lg:flex lg:justify-between">
		<h2 class="text-lg lg:text-xl order-last">1982 – 1991</h2>
		<div class="lg:w-3/5 lg:pr-8 md:text-md lg:text-base xl:text-md">
			<p class="mt-0">FES was founded by an engineer who previously was a powder distributor for an explosives manufacturer in 1982. The blasting industry was largely segregated as either a drilling company or a blasting company and the founder thought “why not combine both” and so Florida Energy Services, Inc. was born.</p>
			<p>The concept grew and FES offered turnkey drill/blast packages in throughout Broward County and central Florida. We continued to distribute explosives to select end users such as the sugar industry that utilized explosives for blasting ditches for watering their crops.</p>
		</div>
