<section class="mb-8 p-4 md:p-8">
	<article class="max-w-7xl mx-auto lg:flex lg:justify-between">
		<h2 class="text-lg lg:text-xl">1992 – 1998</h2>
		<div class="lg:w-3/5 lg:pr-8 md:text-md lg:text-base xl:text-md">
			<p class="mt-0">In 1992, the company sought expansion into the Naples/ Ft. Myers market and hired Rory Simons who had local contractor knowledge over the prior 15 years.</p>
			<p>There were 6 other contractors in the business when the company started in southwest Florida but within several years all of them ceased to do business except FES. They left the market for various reasons, from not breaking the rock properly, violating regulations or not addressing neighbor’s property damage complaints properly.</p>
			<p>Florida Energy Services, Inc. has always dealt with issues in an open and honest way. We do not want our business to affect our contractor customer in a negative manner. Our means of doing business has always been to conduct business in a professional and positive way.</p>
			<p>This has provided federal, state, county and local regulators with a comfort level that they know that FES will conduct our business in a safe and consistent manner and staying well within the boundaries of the laws and regulations that we are governed by.</p>
			<p>“Our goal is to <a href="/safety">safely</a> provide our customers with what they need at a price and quality that they can expect and in a timely manner. We warrant our work that if the customer is not satisfied with what we have provided and as expected, FES will redo the work at no cost to our customer.”</p>
			<p>In 1998, the founder of the company passed away in a small plane crash. Rory Simons then bought the business and continued to operate it.</p>
		</div>
	</article>
</section>

<section class="bg-black text-white mb-8 p-4 py-8 md:px-8 md:py-16">
