		<h2 class="text-lg lg:text-xl order-last">1999 – 2013</h2>
		<div class="lg:w-3/5 lg:pr-8 md:text-md lg:text-base xl:text-md">
			<p class="mt-0">The great recession affected the business severely from late 2006 to late 2012.  We went from 7 crews down to none for most of those years. The 15 most valued employees were kept employed with the quarry work helping us to survive, but barely.</p>
			<p>By early 2013, there was a huge pent-up demand by national builders to get back into community development in southwest Florida. FES began to flourish.</p>
			<p>In other early work, we operated a drill at a large quarry in Freeport, Grand Bahamas from 2001 to 2011. In 2007, we drilled/blasted for a private island development in The Turks & Caicos.</p>
			<p>As opportunities arise, we look for Caribbean work that is not risky. Most island projects are risky, but we are not afraid to look at them as we do have the experience of drilling off barges and doing remote and challenging projects.</p>
